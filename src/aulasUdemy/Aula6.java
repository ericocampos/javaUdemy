package aulasUdemy;

/**
 * @author Erico
 *
 * Using Variables
 * 
 */
public class Aula6 {
	public static void main(String[] args) {
		int myNumber = 88;		
		System.out.println("O int é: " + myNumber);
		
		short myShort= 848;		
		System.out.println("O short é: " + myShort);
		
		long myLong = 9797;		
		System.out.println("O long é: " + myLong);
		
		double myDouble = 7.3232;		
		System.out.println("O double é: " + myDouble);
		
		float myFloat = 324.45f;		
		System.out.println("O float é: " + myFloat);
		
		char myChar = 'y';		
		System.out.println("O char é: " + myChar);
		
		boolean myBoolean = true;		
		System.out.println("O boolean é: " + myBoolean);
		
		byte myByte = 127;
		System.out.println("O byte é: " + myByte);
	}

}
